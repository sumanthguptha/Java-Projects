
public class Charliteral {

	public static void main(String[] args) {
		System.out.println('A');// it takes 2 bytes data
		System.out.println('!');
		System.out.println('+');
		System.out.println('?');
		
		System.out.println('\u0041');// unicode value for A
		System.out.println('\u0066');// unicode value for f
		
		System.out.println('\t');// used for tab
		System.out.println('\n');// used for new line



	}

}
